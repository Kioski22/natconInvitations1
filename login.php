        <div class="form_container">
            <form action="" method="post">
            <h2>Login</h2>
            <?php if (isset($error)): ?>
                <div class="error"><?php echo $error; ?></div>
            <?php endif; ?>
            <label for="email">Email:</label>
            <input type="email" name="email" required>
